#include <stdio.h>

int main() {
    char a = -128;  // -128~127
    /*
    10000000 00000000 00000000 10000000 ~ -128的原码
    11111111 11111111 11111111 10000000 ~ -128的补码
    10000000 ~ char截断
    */
    printf("%u\n", a);  // 4294967168
    /*
    11111111 11111111 11111111 10000000 ~ char提升到有符号int，高位补符号位
    %u 无符号int
    输出4294967168
    */

    return 0;
}
